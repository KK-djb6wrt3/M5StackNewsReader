const uint16_t* getIcon(IconID eID, uint16_t* pWidth, uint16_t* pHeight) {
  const uint16_t* pHead = NULL;
  switch (eID) {
    case ICN_BATT_FULL:
      pHead = s_pBattFull;
      break;
    case ICN_BATT_75:
      pHead = s_pBatt75;
      break;
    case ICN_BATT_50:
      pHead = s_pBatt50;
      break;
    case ICN_BATT_25:
      pHead = s_pBatt25;
      break;
    case ICN_CHRG_FULL:
      pHead = s_pChrgFull;
      break;
    case ICN_CHRG_75:
      pHead = s_pChrg75;
      break;
    case ICN_CHRG_50:
      pHead = s_pChrg50;
      break;
    case ICN_CHRG_25:
      pHead = s_pChrg25;
      break;
    default:
      break;
  }

  const uint16_t* pBuf = NULL;
  uint16_t width = 0U, height = 0U;
  if (pHead != NULL) {
    width = pHead[0];
    height = pHead[1];
    pBuf  = &pHead[2];
  }

  if (pWidth != NULL) {
    *pWidth = width;
  }
  if (pHeight != NULL) {
    *pHeight = height;
  }

  return pBuf;
}
